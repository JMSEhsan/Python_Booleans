print("print(5 > 3) : ", 5 > 3)
print("print(5 == 3):", 5 == 3)
print("print(5 < 3)", 5 < 3)
print()
print("print(bool(5 < 3)): ", bool(5 < 3))
print()
print("print(bool(\"Hello\"):", bool("Hello"))
print("print(bool(5):", bool(5))